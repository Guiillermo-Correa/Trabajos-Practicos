#ifndef FUNCIONES_H_INCLUDED
#define FUNCIONES_H_INCLUDED


/** \brief suma dos numeros
 *
 * \param numeroUno float
 * \param numeroDos float
 * \return resultado de la suma.
 *
 */
void sumar (float numeroUno, float numeroDos);

/** \brief resto dos numeros
 *
 * \param numeroUno float
 * \param numeroDos float
 * \return resultado de la resta
 *
 */
void restar(float numeroUno, float numeroDos);


/** \brief divido dos numeros
 *
 * \param numeroUno float
 * \param numeroDos float
 * \return resultado de la division.
 *
 */
void dividir(float numeroUno, float numeroDos);

/** \brief multiplico dos numeros
 *
 * \param numeroUno float
 * \param numeroDos float
 * \return resultado de la multiplicaion.
 *
 */
void multplicacion(float numeroUno, float numeroDos);

/** \brief saco el factorial de un numero
 *
 * \param numeroUno float
 *
 * \return resultado del factorial
 *
 */
void factorial(float numeroUno);


#endif // FUNCIONES_H_INCLUDED
